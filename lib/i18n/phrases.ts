export function translatePhraseToEnglish(text: string) {
  const trimmed = text.trim();
  if (!trimmed) return text;
  const exact = zhToEnPhrases[trimmed];
  if (exact) return text.replace(trimmed, exact);
  const tabCount = trimmed.match(/^(.+?)\s*\((\d+)\)$/);
  if (tabCount && zhToEnPhrases[tabCount[1]]) {
    return text.replace(trimmed, `${zhToEnPhrases[tabCount[1]]} (${tabCount[2]})`);
  }
  const tabOpen = trimmed.match(/^(.+?)\s*\($/);
  if (tabOpen && zhToEnPhrases[tabOpen[1]]) {
    return text.replace(trimmed, `${zhToEnPhrases[tabOpen[1]]} (`);
  }
  const tabOpenCount = trimmed.match(/^(.+?)\s*\((\d+)$/);
  if (tabOpenCount && zhToEnPhrases[tabOpenCount[1]]) {
    return text.replace(trimmed, `${zhToEnPhrases[tabOpenCount[1]]} (${tabOpenCount[2]}`);
  }
  const typeRatio = trimmed.match(/^(.+?)\s*\((\d+\/\d+)\)$/);
  if (typeRatio && zhToEnPhrases[typeRatio[1]]) {
    return text.replace(trimmed, `${zhToEnPhrases[typeRatio[1]]} (${typeRatio[2]})`);
  }
  const typeRatioOpen = trimmed.match(/^(.+?)\s*(\d+\/\d+)$/);
  if (typeRatioOpen && zhToEnPhrases[typeRatioOpen[1]]) {
    return text.replace(trimmed, `${zhToEnPhrases[typeRatioOpen[1]]} ${typeRatioOpen[2]}`);
  }
  const pagePeople = trimmed.match(/^第\s*(\d+)\s*\/\s*(\d+)\s*页（共\s*(\d+)\s*人）$/);
  if (pagePeople) {
    return text.replace(trimmed, `Page ${pagePeople[1]} / ${pagePeople[2]} (${pagePeople[3]} users)`);
  }
  const pagePrefix = trimmed.match(/^第\s*(\d+)$/);
  if (pagePrefix) {
    return text.replace(trimmed, `Page ${pagePrefix[1]}`);
  }
  const pagePeopleSuffix = trimmed.match(/^页（共\s*(\d+)\s*人）$/);
  if (pagePeopleSuffix) {
    return text.replace(trimmed, `(${pagePeopleSuffix[1]} users)`);
  }
  if (trimmed === "页") {
    return text.replace(trimmed, "");
  }
  const pageItems = trimmed.match(/^第\s*(\d+)\s*\/\s*(\d+)\s*页（共\s*(\d+)\s*条）$/);
  if (pageItems) {
    return text.replace(trimmed, `Page ${pageItems[1]} / ${pageItems[2]} (${pageItems[3]} items)`);
  }
  const pageOnly = trimmed.match(/^第\s*(\d+)\s*\/\s*(\d+)\s*页$/);
  if (pageOnly) {
    return text.replace(trimmed, `Page ${pageOnly[1]} / ${pageOnly[2]}`);
  }
  const pageOnlyWithoutSuffix = trimmed.match(/^第\s*(\d+)\s*\/\s*(\d+)$/);
  if (pageOnlyWithoutSuffix) {
    return text.replace(trimmed, `Page ${pageOnlyWithoutSuffix[1]} / ${pageOnlyWithoutSuffix[2]}`);
  }
  const questionCount = trimmed.match(/^(\d+)\s*道题目$/);
  if (questionCount) {
    return text.replace(trimmed, `${questionCount[1]} questions`);
  }
  const questionCountShort = trimmed.match(/^(\d+)\s*道$/);
  if (questionCountShort) {
    return text.replace(trimmed, `${questionCountShort[1]} questions`);
  }
  const previewTitle = trimmed.match(/^预览：(.+)（(\d+)\s*道）$/);
  if (previewTitle) {
    return text.replace(trimmed, `Preview: ${previewTitle[1]} (${previewTitle[2]} questions)`);
  }
  const mixedPreviewTitle = trimmed.match(/^Preview:(.+)（(\d+)\s*道）$/);
  if (mixedPreviewTitle) {
    return text.replace(trimmed, `Preview: ${mixedPreviewTitle[1].trim()} (${mixedPreviewTitle[2]} questions)`);
  }
  const questionCountParen = trimmed.match(/^（(\d+)\s*道）$/);
  if (questionCountParen) {
    return text.replace(trimmed, `(${questionCountParen[1]} questions)`);
  }
  const questionCountParenAscii = trimmed.match(/^\((\d+)\s*道\)$/);
  if (questionCountParenAscii) {
    return text.replace(trimmed, `(${questionCountParenAscii[1]} questions)`);
  }
  const previewPrefix = trimmed.match(/^Preview:(.+)$/);
  if (previewPrefix) {
    return text.replace(trimmed, `Preview: ${previewPrefix[1].trim()}`);
  }
  const answerPrefix = trimmed.match(/^答：(.+)$/);
  if (answerPrefix) {
    return text.replace(trimmed, `Answer: ${answerPrefix[1]}`);
  }
  const selected = trimmed.match(/^已选\s*(\d+)\s*人：?$/);
  if (selected) {
    return text.replace(trimmed, `Selected ${selected[1]} users:`);
  }
  const judgeConnectivity = trimmed.match(/^评分器连通性测试(中|通过|失败)\.\.\.$/);
  if (judgeConnectivity) {
    return text.replace(trimmed, `Judge connectivity test ${judgeConnectivity[1] === "中" ? "in progress" : judgeConnectivity[1] === "通过" ? "passed" : "failed"}...`);
  }
  const judgeConnectivityNoDots = trimmed.match(/^评分器连通性测试(中|通过|失败)$/);
  if (judgeConnectivityNoDots) {
    return text.replace(trimmed, `Judge connectivity test ${judgeConnectivityNoDots[1] === "中" ? "in progress" : judgeConnectivityNoDots[1] === "通过" ? "passed" : "failed"}`);
  }
  const reason = trimmed.match(/^原因：(.+)$/);
  if (reason) {
    return text.replace(trimmed, `Reason: ${reason[1]}`);
  }
  const requestRejectReason = trimmed.match(/^申请未通过，原因：(.+)$/);
  if (requestRejectReason) {
    return text.replace(trimmed, `Request rejected. Reason: ${requestRejectReason[1]}`);
  }
  const newApplicationBody = trimmed.match(/^用户\s+(.+?)\s+提交了发布权限申请，请前往管理控制台审批。$/);
  if (newApplicationBody) {
    return text.replace(trimmed, `User ${newApplicationBody[1]} submitted a publisher access request. Please review it in the admin console.`);
  }
  const model = trimmed.match(/^模型：(.+)$/);
  if (model) {
    return text.replace(trimmed, `Model: ${model[1]}`);
  }
  const modelConnectivityTitle = trimmed.match(/^模型连通性测试\s*[—-]\s*(.+)$/);
  if (modelConnectivityTitle) {
    return text.replace(trimmed, `Model Connectivity Test - ${modelConnectivityTitle[1]}`);
  }
  if (trimmed === "模型：") {
    return text.replace(trimmed, "Model:");
  }
  const difficulty = trimmed.match(/^难度：(.+)$/);
  if (difficulty) {
    return text.replace(trimmed, `Difficulty: ${zhToEnPhrases[difficulty[1]] ?? difficulty[1]}`);
  }
  if (trimmed === "难度：") {
    return text.replace(trimmed, "Difficulty:");
  }
  const difficultySuffix = trimmed.match(/^(.*)【难度：(.+)】$/);
  if (difficultySuffix) {
    return text.replace(trimmed, `${difficultySuffix[1]} [Difficulty: ${zhToEnPhrases[difficultySuffix[2]] ?? difficultySuffix[2]}]`);
  }
  const sampleSuffix = trimmed.match(/^(.*)【来自样例题库】$/);
  if (sampleSuffix) {
    return text.replace(trimmed, `${sampleSuffix[1]} [from sample question bank]`);
  }
  const testPassed = trimmed.match(/^测试通过(.+)$/);
  if (testPassed) {
    return text.replace(trimmed, `Test passed ${testPassed[1]}`);
  }
  return text;
}
